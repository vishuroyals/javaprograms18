package operators;
//Operators are Special symbols used to perform operations on varaible and values 
public class ArithmeticOperator {
public static void main(String[] args) {
	int n1=10;
	int n2=5;
	//Arithmetic Operators
	System.out.println("Addition:"+(n1+n2));//15
	System.out.println("Subtraction:"+(n1-n2));//5
	System.out.println("Multiplication:"+(n1*n2));//50
	System.out.println("Division:"+(n1/n2));//2(quotient)
	System.out.println("Modular Division:"+(n1%n2));//1(remainder)
}
}
//Assignment Operator
//x=10(assign);
//x+=10/x=x+10(Addition);
//x-=10/x=x-10(subtraction);
//x*=10/x=x*10;(Multiplication)
//x/=10/x=x/10(Division) 
//x%=10/x=x%10(Modular division)